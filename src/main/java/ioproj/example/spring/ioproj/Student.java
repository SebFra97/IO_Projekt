package ioproj.example.spring.ioproj;

public class Student {

    public final Long id;
    public final String name;
    public final String number;
    public final String group1;

    public Student(Long id, String name, String number, String group1) {
        this.id = id;
        this.name = name;
        this.number = number;
        this.group1 = group1;
    }
}
